class Point():
    
    def __init__(self,x,y):
        self.__x = x
        self.__y = y
        
    def getX(self):
        return self.__x
    
    def getY(self):
        return self.__y
      
    def setX(self,X):
        self.__x = X
    
    def setY(self,Y):
        self.__y = Y
    



        
        
